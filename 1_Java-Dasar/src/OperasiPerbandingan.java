public class OperasiPerbandingan {
    public static void main(String[] args) {
        int a = 100;
        int b = 100;

        boolean c = a > b;

        System.out.println("a > b => " + c);
        System.out.println("a < b => " + (a < b));
        System.out.println("a >= b => " + (a >= b));
        System.out.println("a <= b => " + (a <= b));
        System.out.println("a == b => " + (a == b));
        System.out.println("a != b => " + (a != b));

    }
}
