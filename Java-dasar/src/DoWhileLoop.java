public class DoWhileLoop {
    public static void main(String[] args) {
        // Lakukan dulu diawal baru periksa kondisi
        var counter = 20;

        do {
            System.out.println("Perulangan ke-" + counter);
            counter--;
        } while (counter >= 10); {
            System.out.println("Angka ke-" + counter);
        }
    }
}
