package muhammadkpn;

public class MathApp {
    public static void main(String[] args) {
        // Math class berisikan banyak sekali static method untuk operasi matematika (trigonometri, logarithm, etc)
        // Get min value
        var min = Math.min(1302, 3132);
        System.out.println(min);

        // Get max value
        var max = Math.max(331, 3330);
        System.out.println(max);

        // Get Pi value
        System.out.println(Math.PI);
    }
}
